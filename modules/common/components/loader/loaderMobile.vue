<script>
  import TimelineMax from 'gsap/umd/TimelineMax'
  import EasePack from 'gsap/umd/EasePack'

  export default {
    props: {
      fetched: {
        type: Boolean,
        required: false,
        default: false
      },
      loading: {
        type: Boolean,
        required: false,
        default: true
      }
    },
    data () {
      return {
        loadingAnimation: null
      }
    },
    watch: {
      fetched: function (newVal) {
        if (newVal) {
          setTimeout(() => {
            this.stopLoading()
          }, 4000)
        }
      }
    },
    mounted () {
      this.startLoadingAnimation()
      // this.slideUp()
    },
    methods: {
      stopLoading () {
        const t = new TimelineMax()

        this.loadingAnimation.kill()

        this.$refs.loaderIcon.style.opacity = 0
        // this.$refs.loaderBackground.style.opacity = 0

        t.to(
          this.$refs.loaderContainer,
          0.4,
          {
            opacity: 0,
            visibility: 'hidden',
            ease: EasePack.Sine.easeIn
          }
        )

        t.to(
          this.$refs.loaderContainer,
          0.1,
          {
            visibility: 'hidden',
            ease: EasePack.Sine.easeIn
          }
        )
      },
      startLoadingAnimation () {
        const t = new TimelineMax({ repeat: -1 })

        t.to(
          this.$refs.squareMain,
          0.8,
          {
            opacity: 0,
            ease: EasePack.Sine.easeIn
          },
          'loader-start'
        )

        t.to(
          this.$refs.squareSecondary,
          1.3,
          {
            opacity: 1,
            ease: EasePack.Sine.easeOut
          },
          'loader-start'
        )

        t.to(
          this.$refs.squareMain,
          1.3,
          {
            opacity: 1,
            ease: EasePack.Sine.easeOut
          },
          'loader-finish'
        )

        t.to(
          this.$refs.squareSecondary,
          0.8,
          {
            opacity: 0,
            ease: EasePack.Sine.easeIn
          },
          'loader-finish'
        )

        this.loadingAnimation = t
      }
